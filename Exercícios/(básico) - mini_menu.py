# Programa que simula um mini menu usando o laço while.

while (True):
    print('Escolha sua opção: \n1 \n2 \n3 \n4 - Para voltar!')
    variavel = int(input(""))
    if (variavel == 1):
        print('opção 1')
        break
    elif (variavel == 2):
        print('opção 2')
        break
    elif (variavel == 3):
        print('opção 3')
        break
